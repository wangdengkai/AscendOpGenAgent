#!/usr/bin/env python3
"""lingxi_profiling_collector.py — Collect performance CSV data for Lingxi operator variants.

Usage:
    python3 lingxi_profiling_collector.py <task_dir> \
        --output-dir <profiling_output_dir> \
        [--device 0] \
        [--warmup 5] \
        [--repeat 10] \
        [--output result.json]

This script:
1. Tries `msprof op` (hardware-level profiling) first — captures real pipeline
   utilization data (aiv_vec_ratio, aiv_mte2_ratio, etc.) via ACL runtime
   interception of aclrtLaunchKernelWithHostArgs.
2. Falls back to host-side timing (time.perf_counter) if msprof is unavailable
   or fails.
3. Outputs JSON: {"success": bool, "profiling_dir": str, "op_summary_csv": str,
   "mode": "msprof_op"|"host_timing"}

NOTE: Lingxi operators use pybind11 direct kernel launch (not GE dispatch).
torch_npu.profiler cannot capture TASK-level data for this launch path, but
`msprof op` works because it intercepts at the ACL runtime layer.
"""

